#include "drivers/out/alarm.h"
#include <util/delay.h>



// =============================================================================
// Application
// =============================================================================

void application__init(void) {
    alarm__init();
}


// =============================================================================
// Entry point.
// =============================================================================
int main(void) {
    application__init();

    // run background tasks
    for(;;) {
        alarm__set(1);
        _delay_ms(1000);
        alarm__set(0);
        _delay_ms(1000);
    }

    return 0;
}
