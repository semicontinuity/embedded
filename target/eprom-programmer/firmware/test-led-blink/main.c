// =============================================================================
// LED blink test.
// =============================================================================

#include <avr/io.h>
#include <util/delay.h>



// =============================================================================
// Entry point.
// =============================================================================
int main(void) {
    DDRD = 0x01;

    for(;;) {
        PORTD = 0x01;
        _delay_ms(1000);
        PORTD = 0x00;
        _delay_ms(1000);
    }

    return 0;
}
