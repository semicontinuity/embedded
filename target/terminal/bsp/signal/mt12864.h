#define MT12864_DATA__PORT      A


/*--------------------------------------------------------------------------------------------*/
/* Line E connected to PD6.                                                                   */
/* Active level - high                                                                        */
/*--------------------------------------------------------------------------------------------*/
#define MT12864_E__PORT         D
#define MT12864_E__PIN          6


/*--------------------------------------------------------------------------------------------*/
/* Line A0 (data/command) is connected to PC0.                                                */
/* "1" - data transmission, "0" - command transmission.                                       */
/*--------------------------------------------------------------------------------------------*/
#define MT12864_A0__PORT        C
#define MT12864_A0__PIN         0

/*--------------------------------------------------------------------------------------------*/
/* Line RW (read/write) is connected to PD5.                                                  */
/* "1" - read cycle, "0" - write cycle.                                                       */
/*--------------------------------------------------------------------------------------------*/
#define MT12864_RW__PORT        D
#define MT12864_RW__PIN         5

/*--------------------------------------------------------------------------------------------*/
/* Line CS2 (select chip 2) is connected to PC7. (not PC6?)                                   */
/* Active level - low                                                                         */
/*--------------------------------------------------------------------------------------------*/
#define MT12864_CS2__PORT       C
#define MT12864_CS2__PIN        7

/*--------------------------------------------------------------------------------------------*/
/* Line CS1 (select chip 1) is connected to PC6. (not PC7?)                                   */
/* Active level - low                                                                         */
/*--------------------------------------------------------------------------------------------*/
#define MT12864_CS1__PORT       C
#define MT12864_CS1__PIN        6

/*--------------------------------------------------------------------------------------------*/
/* Line RES (reset) is connected to PC1.                                                      */
/* Active level - low                                                                         */
/*--------------------------------------------------------------------------------------------*/
#define MT12864_RES__PORT       C
#define MT12864_RES__PIN        1
