#include <avr/io.h>
#include <util/delay.h>



// =============================================================================
// Application
// =============================================================================

void application__init(void) {
    DDRB = _BV(5);
}


// =============================================================================
// Entry point.
// =============================================================================
int main(void) {
    application__init();

    // run background tasks
    for(;;) {
        PORTB = _BV(5);
        _delay_ms(1000);
        PORTB = 0;
        _delay_ms(1000);
    }

    return 0;
}
