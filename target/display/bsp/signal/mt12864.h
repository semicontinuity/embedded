#define MT12864_DATA__PORT      B


/*--------------------------------------------------------------------------------------------*/
/* Line E connected to PC0.                                                                   */
/* Active level - high                                                                        */
/*--------------------------------------------------------------------------------------------*/
#define MT12864_E__PORT         C
#define MT12864_E__PIN          0

/*--------------------------------------------------------------------------------------------*/
/* Line A0 (data/command) is connected to PC1.                                                */
/* "1" - data transmission, "0" - command transmission.                                       */
/*--------------------------------------------------------------------------------------------*/
#define MT12864_A0__PORT        C
#define MT12864_A0__PIN         1

/*--------------------------------------------------------------------------------------------*/
/* Line RW (read/write) is connected to PC2.                                                  */
/* "1" - read cycle, "0" - write cycle.                                                       */
/*--------------------------------------------------------------------------------------------*/
#define MT12864_RW__PORT        C
#define MT12864_RW__PIN         2

/*--------------------------------------------------------------------------------------------*/
/* Line CS2 (select chip 2) is connected to PC3.                                              */
/* Active level - low                                                                         */
/*--------------------------------------------------------------------------------------------*/
#define MT12864_CS2__PORT       C
#define MT12864_CS2__PIN        3

/*--------------------------------------------------------------------------------------------*/
/* Line CS1 (select chip 1) is connected to PC4.                                              */
/* Active level - low                                                                         */
/*--------------------------------------------------------------------------------------------*/
#define MT12864_CS1__PORT       C
#define MT12864_CS1__PIN        4

/*--------------------------------------------------------------------------------------------*/
/* Line RES (reset) is connected to PC5.                                                      */
/* Active level - low                                                                         */
/*--------------------------------------------------------------------------------------------*/
#define MT12864_RES__PORT       C
#define MT12864_RES__PIN        5

/*--------------------------------------------------------------------------------------------*/
/* Backlight control is connected to PC6.                                                     */
/* "1" - backlight on, "0" - backlight off.                                                   */
/*--------------------------------------------------------------------------------------------*/
#define MT12864_BACKLIGHT__PORT C
#define MT12864_BACKLIGHT__PIN  6
